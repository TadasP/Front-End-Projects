                    <p>Studies have considered how the form we take in virtual reality can affect our perception and actions. One study suggests that embodying the body of a young child can influence perception of object sizes such that objects are perceived as being much larger than if the objects were perceived by an individual embodying an adult body. Similarly, another study has found that Caucasian individuals who embodied the form of a dark-skinned avatar performed a drumming task with a more varied style than when they were represented by a pair of white-shaded hands and in comparison to individuals who embodied a light-skin avatar. As a whole, these works suggest that immersive virtual reality can create body-transfer illusions capable of influencing how humans respond to different circumstances.</p>
                    <p>Current VR technology most commonly uses virtual reality headsets or multi-projected environments, sometimes in combination with physical environments or props, to generate realistic images, sounds and other sensations that simulate a user's physical presence in a virtual or imaginary environment. A person using virtual reality equipment is able to "look around" the artificial world, move around in it, and interact with virtual features or items. The effect is commonly created by VR headsets consisting of a head-mounted display with a small screen in front of the eyes, but can also be created through specially designed rooms with multiple large screens.</p>
                    <p>All modern VR displays are based on technology developed for smartphones including: gyroscopes and motion sensors for tracking head, hand, and body positions; small HD screens for stereoscopic displays; and small, lightweight and fast processors. These components led to relative affordability for independent VR developers, and lead to the 2012 Oculus Rift kickstarter offering the first independently developed VR headset.</p>
                    <p class="quote">"I love Diana Ikasalaitė - Tadas P"</p>
                    <p>By 2016 there were at least 230 companies developing VR-related products. Facebook has 400 employees focused on VR development; Google, Apple, Amazon, Microsoft, Sony and Samsung all had dedicated AR and VR groups. Dynamic binaural audio was common to most headsets released that year. However, haptic interfaces were not well developed, and most hardware packages incorporated button-operated handsets for touch-based interactivity. Visually, displays were still of a low-enough resolution and frame-rate that images were still identifiable as virtual. On April 5, 2016, HTC shipped its first units of the HTC VIVE SteamVR headset. This marked the first major commercial release of sensor-based tracking, allowing for free movement of users within a defined space.</p>
