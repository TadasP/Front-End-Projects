                    <p>It was easily the loudest and possibly most dramatic launch from Florida's "Space Coast" since NASA's space shuttle was retired in 2011 with the Falcon Heavy, made up of three strapped-together core stages powered by nine engines each, putting on a dazzling show for tourists and area residents jamming nearby roads and beaches. Spectacular as it was, the launching was just the appetizer for a long-awaited test flight. Eight minutes after the rocket took off, two of the three Falcon 9 core stages that helped power the vehicle out of the lower atmosphere made rocket-powered descents to side-by-side touchdowns at the Cape Canaveral Air Force Station, generating thunderous sonic booms as they slowed for landing.</p>
                    <p> The two passengers would be the only people on board what SpaceX expects to be about a week-long trip around the Moon, according to Musk, who spoke with reporters during a phone conference. "This would be a long loop around the moon. ... It would skim the surface of the moon, go quite a bit further out into deep space and then loop back to Earth," Musk said during the teleconference. "So I'm guessing, distance-wise, maybe [about 500,000 to 650,000 kilometers]."</p>
                    <p>n May 2012, SpaceX announced that Intelsat had signed the first commercial contract for a Falcon Heavy flight. It was not confirmed at the time when the first Intelsat launch would occur, but the agreement will have SpaceX delivering satellites to geosynchronous transfer orbit (GTO). In August 2016, it emerged that this Intelsat contract had been reassigned to a Falcon 9 Full Thrust mission to deliver Intelsat 35e into orbit in the third quarter of 2017. Performance improvements of the Falcon 9 vehicle family since the 2012 announcement, advertising 8,300 kg to GTO for its expendable flight profile, enable the launch of this 6,000 kg satellite without upgrading to a Falcon Heavy variant. In 2014, Inmarsat booked 3 launches with Falcon Heavy, but due to delays they switched a payload to Ariane 5 for 2017.Similarly to the Intelsat 35e case, another satellite from this contract, Inmarsat 5-F4, was switched to a Falcon 9 Full Thrust thanks to the increased liftoff capacity.The remaining contract covers the launch of Inmarsat 6-F1 in 2020 on a Falcon 9.</p>
                    <p class="quote">Musk announced that SpaceX would attempt to fly two private citizens on a free-return trajectory around the Moon in late 2018, somewhat paralleling Apollo 8.</p>
                    <p>In 2011, NASA Ames Research Center proposed a Mars mission called Red Dragon that would use a Falcon Heavy as the launch vehicle and trans-Martian injection vehicle, and a variant of the Dragon capsule to enter the Martian atmosphere. The proposed science objectives were to detect biosignatures and to drill 1.0 meter (3.3 ft) or so underground, in an effort to sample reservoirs of water ice known to exist under the surface. The mission cost as of 2011 was projected to be less than US$425,000,000, not including the launch cost. SpaceX 2015 estimation was 2,000–4,000 kg (4,400–8,800 lb) to the surface of Mars, with a soft retropropulsive landing following a limited atmospheric deceleration using a parachute and heat shield.</p>
