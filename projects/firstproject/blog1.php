                    <p>At its core, your content marketing strategy is your “why.” Why you are creating content, who you are helping, and how you will help them in a way no one else can. Organizations typically use content marketing to build an audience and to achieve at least one of these profitable results: increased revenue, lower costs, or better customers. On the other hand, content strategy delves deeper into (in Kristina Halvorson’s words) the “creation, publication, and governance of useful, usable content.” Note that content strategy often goes beyond the scope of a content marketing strategy, as it helps businesses manage all of the content they have.</p>
                    <p>My research (at Altimeter Group), corroborated by that of several other studies, indicates that currently 70 percent of companies practicing content marketing lack a documented strategy. But thankfully, this is slowly changing as the need to align content with actual goals, processes and procedures comes into focus. What are the steps to outlining that documented strategy? The following is a list of my asks the moment I’m brought into an organization to help them develop a content strategy road map. The first, and most critical part, is goals. What is content trying to achieve? What are the business reasons for creating and publishing content, and how are these goals aligned to broader company priorities? This critical first step is determining the big “Why” of content. Without the why, there can be no strategy.</p>
                    <p>Part 2 may be secondary, but it’s of equal importance. It answers the question “How?” People, process, governance, tools, technologies, assets — all of these and more must be present and accounted for, aligned and communicated to numerous stakeholders. How is ongoing, but adheres to a broad procedural schema. In order to determine Why and get to the How, these are my “Day One” requests when beginning a content strategy engagement.</p>
                    <p class="quote">We are not simply in the universe, we are part of it. We are born from it. One might even say we have been empowered by the universe to figure itself out — and we have only just begun.</p>
                    <p>A content audit is a painstaking, exacting exercise that many would be only too happy to skip. But you can’t. If you don’t know where you are, you can’t chart the journey forward. A content audit is both a quantitative and, more importantly, a qualitative analysis of all the content for which your organization is responsible. In order to conduct an audit, you’ll need a list of all your public-facing online properties, from websites to social media. When I conduct an audit I want to see your email marketing, your ad campaigns. I even (this surprises many of my clients) want to examine offline collateral, perhaps that big annual report or research study undertaken annually or semiannually. It’s not enough to just have at the content itself. I’ll also request access to analytics software (Web, social, email, and so on). The purpose of an audit isn’t just to evaluate whether or not I like your content. I want to see if it’s being seen, found, used, shared and amplified — or not. A good audit (they vary by purpose and type of engagement) is a 50-point diagnostic. They’re very deep and reveal often-surprising insights, not just about the content itself, but also requirements for the processes and technologies to create and sustain the flow of content. The goal is to define gaps and problems, as well as to identify strengths, and develop specific recommendations for improvement.</p>
