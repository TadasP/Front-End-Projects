                    <p>E-mail marketing can be massively valuable for your business. As more and more marketers lose sleep sweating on Facebook’s next steps towards reducing organic reach to absolute zero, smart businesses are learning that there’s significant and present opportunities in the ‘rented land’ of your social media properties. Hosting discussions on your owned assets, building direct connections between your brand and your audience is often a safer and more binding arrangement.</p>
                    <p>But as with establishing an audience anywhere, building and maintaining an e-mail relationship takes effort. You need to deliver consistent value and you need to stay in constant, or at least regular, contact. If you’re not in touch with your e-mail contacts once a week or once a month, the effectiveness of your e-mail marketing can be lost. The best e-mail marketers generally stick to a very rigid routine, posting updates on a certain day, at a certain time, this consistency enables them to stay in the recipient’s mind and helps them establish an expectation of what they’ll get as a result of signing up to their updates. Post erratically, and you risk fading into the junk folder, or worse, falling victim to the unsubscribe. So how do you manage it? How do you ensure you’re maintaining a constant content flow in order to fuel updates that will be relevant and will increase click-through rates, and ultimately, convert subscribers in loyal customers. Here’s a few tips on how to maximise your e-mail outreach efforts and do it in less than an hour a day.</p>
                    <p>Probably the most time-consuming element of a regular e-mail marketing routine is filling it with relevant content. If you’re a prominent blogger then it’s a little easier, as you can include your latest posts in your send, but you also need to consider how many of your subscribers are also subscribers to your blog – do they need to read your newsletter informing them of your latest post when they already got a notification of it when you published? As with maintaining an active social media presence, finding the right content, and the right topics of interest to your e-mail subscribers is key to building a reliable outreach presence.</p>
                    <p class="quote"> For this example, we’ll go with a once weekly newsletter, sent out every Thursday at 11am. The day and time is important, as subscribers are likely to respond better to consistency, knowing when to expect your content.</p>
                    <p>So now you have a purpose, a schedule and places to source content information from, the next step is allocating the right amount of time to get it done. And time, as we all know, is something not many of us have a lot of. This is particularly true of small to medium business owners who are often wearing many hats – for your e-mail outreach to be effective, you need to be consistent and you need to add value, so it’s important that you do set aside dedicated time to get it done.</p>
