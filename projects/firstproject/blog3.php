                    <p>A content audit is a painstaking, exacting exercise that many would be only too happy to skip. But you can’t. If you don’t know where you are, you can’t chart the journey forward. A content audit is both a quantitative and, more importantly, a qualitative analysis of all the content for which your organization is responsible. In order to conduct an audit, you’ll need a list of all your public-facing online properties, from websites to social media. When I conduct an audit I want to see your email marketing, your ad campaigns. I even (this surprises many of my clients) want to examine offline collateral, perhaps that big annual report or research study undertaken annually or semiannually. It’s not enough to just have at the content itself. I’ll also request access to analytics software (Web, social, email, and so on). The purpose of an audit isn’t just to evaluate whether or not I like your content. I want to see if it’s being seen, found, used, shared and amplified — or not. A good audit (they vary by purpose and type of engagement) is a 50-point diagnostic. They’re very deep and reveal often-surprising insights, not just about the content itself, but also requirements for the processes and technologies to create and sustain the flow of content. The goal is to define gaps and problems, as well as to identify strengths, and develop specific recommendations for improvement.</p>
