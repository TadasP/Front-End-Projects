                    <p>The definition of "transform" is "to make over to a radically different form, composition, state, or disposition." By definition, then, to be transformed means we have to change. Therefore, in order to experience the transformation of our past, our present, and our future, we must be willing to experience fear for a short while. To allow transformation to happen, we have to be able to "be" with change and the fear that may accompany it. So if we want to be transformed - to change the form of our lives and bodies, to alter our biological and experiential composition, to have a "re-do" with a clean slate, and to change our natural predisposition to life for the better - we need to experience a personal transformation. The clues to how we can enjoy all that are found in the definition of "transformation"–a fundamental alteration in one's system of beliefs. So in order to experience a personal transformation we need to change our beliefs, specifically the beliefs that created the portions of our lives that we would like to have transformed.</p>
